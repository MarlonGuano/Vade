    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Usuario obj = new Usuario();
        obj.setCedula(txtCedula.getText().toUpperCase().trim());
        obj.setNombre(txtNombre.getText().toUpperCase().trim());
        obj.setApellido(txtApellido.getText().toUpperCase().trim());
        obj.setCorreo(txtCorreo.getText().toUpperCase().trim());
        obj.setUsuario(txtUsuario.getText().toUpperCase().trim());
        obj.setContrasena(txtContrasena.getText().toUpperCase().trim());
        Combo cmb = (Combo) cmbTipoUsuario.getSelectedItem();
        obj.setObj(new TipoUsuario(cmb.getId_combo(), cmb.getNombre_combo()));
        new daoUsuario().InsertarUsuario(obj);
        clear();
    }//GEN-LAST:event_btnRegistrarActionPerformed

